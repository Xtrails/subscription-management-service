import axios from 'axios';

import { type ISubscriptionDetails } from '@/shared/model/subscription-details.model';

const baseApiUrl = 'api/subscription-details';

export default class SubscriptionDetailsService {
  find(id: number): Promise<ISubscriptionDetails> {
    return new Promise<ISubscriptionDetails>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  create(entity: ISubscriptionDetails): Promise<ISubscriptionDetails> {
    return new Promise<ISubscriptionDetails>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  update(entity: ISubscriptionDetails): Promise<ISubscriptionDetails> {
    return new Promise<ISubscriptionDetails>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  partialUpdate(entity: ISubscriptionDetails): Promise<ISubscriptionDetails> {
    return new Promise<ISubscriptionDetails>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
